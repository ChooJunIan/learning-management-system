    private void jTextField1_nameFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1_nameFocusGained
        // clear the textfield on focus if the text is "username"
        if (jTextField1_name.getText().trim().toLowerCase().equals("username")) {
            jTextField1_name.setText("");
            jTextField1_name.setForeground(Color.black);
        }
    }//GEN-LAST:event_jTextField1_nameFocusGained

    private void jTextField1_nameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1_nameFocusLost
        if (jTextField1_name.getText().trim().equals("")
                || jTextField1_name.getText().trim().toLowerCase().equals("username")) {
            jTextField1_name.setText("username");
            jTextField1_name.setForeground(new Color(153, 153, 153));
        }
    }//GEN-LAST:event_jTextField1_nameFocusLost

    private void jPasswordField1_passwordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1_passwordFocusGained
        String pass = String.valueOf(jPasswordField1_password.getPassword());

        if (pass.trim().toLowerCase().equals("password")) {
            jPasswordField1_password.setText("");
            jPasswordField1_password.setForeground(Color.black);
        }
    }//GEN-LAST:event_jPasswordField1_passwordFocusGained

    private void jPasswordField1_passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1_passwordFocusLost
        // if the password is equal to password or empty
        // we will set the "password" text in the field
        // on focus lost event

        //get the password text
        String pass = String.valueOf(jPasswordField1_password.getPassword());

        if (pass.trim().equals("")
                || pass.trim().toLowerCase().equals("username")) {
            jPasswordField1_password.setText("password");
            jPasswordField1_password.setForeground(new Color(153, 153, 153));
        }
    }//GEN-LAST:event_jPasswordField1_passwordFocusLost

