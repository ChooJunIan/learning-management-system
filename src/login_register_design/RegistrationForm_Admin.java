    private void jButton2_registerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2_registerMouseClicked
        String databaseURL = "jdbc:derby://localhost:1527/TMS";
        try ( Connection conn = DriverManager.getConnection(databaseURL, "root", "123123")) {
            Statement sta = conn.createStatement();

            var name = jTextField1_name.getText();
            var password = new String(jPasswordField1_password.getPassword());

            if (name.equals("username")) {
                JOptionPane.showMessageDialog(null, "Please enter a name", "InfoBox: ", JOptionPane.ERROR_MESSAGE);
            }
            if (password.equals("password")) {
                JOptionPane.showMessageDialog(null, "Please enter a password", "InfoBox: ", JOptionPane.ERROR_MESSAGE);
            }
            if (!name.equals("username") && !password.equals("password")) {
                if (conn != null) {
                    String query = "Select count(*) from Adminstrator";
                    ResultSet rs = sta.executeQuery(query);
                    rs.next();
                    int count = rs.getInt(1);
                    count++;

                    String sql = "INSERT INTO Adminstrator VALUES (" + count + ", '" + name + "', '" + password + "')";
                    sta.executeUpdate(sql);
                    sta.close();
                    conn.close();
                    JOptionPane.showMessageDialog(null, "Register Successfully!");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }

        setVisible(false);
        new GUI_Admin().setVisible(true);

    }//GEN-LAST:event_jButton2_registerMouseClicked

