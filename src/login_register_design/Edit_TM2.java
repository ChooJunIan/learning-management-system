    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = trMaterial._trMaterial.id;
        String topic = jTextField1_topic.getText();
        String desc = jTextArea1_desc.getText();
        if (topic.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a topic!");
            this.setVisible(false);
            this.setVisible(true);
        }
        if (desc.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter a description!");
            this.setVisible(false);
            this.setVisible(true);
        }
        if (!topic.equals("") && !desc.equals("")) {
            updateTrMaterial(id, topic, desc);
            trMaterial._trMaterial = null;

            JOptionPane.showMessageDialog(this, "Training Material Successfully Edited");
            new Edit_Training_Material().setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

