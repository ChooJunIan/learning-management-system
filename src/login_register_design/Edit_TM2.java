    public void updateTrMaterial(int id, String topic, String desc) {
        String databaseURL = "jdbc:derby://localhost:1527/TMS";
        try ( Connection conn = DriverManager.getConnection(databaseURL, "root", "123123")) {
            Statement sta = conn.createStatement();

            if (conn != null) {
                String sql = "UPDATE ROOT.TRAINING_MATERIAL \n"
                        + "SET ROOT.TRAINING_MATERIAL.TRMATERIAL_TOPIC = '" + topic + "', ROOT.TRAINING_MATERIAL.TRMATERIAL_DESCRIPTION = '" + desc + "' \n"
                        + "WHERE ROOT.TRAINING_MATERIAL.TRMATERIAL_ID = " + id;

                sta.executeUpdate(sql);
                sta.close();
                conn.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

